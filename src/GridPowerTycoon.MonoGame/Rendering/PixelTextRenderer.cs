    public PixelTextRenderer(Texture2D pixel)
    {
        _pixel = pixel;
    }

    public void DrawString(SpriteBatch spriteBatch, string text, Vector2 position, Color color, int scale = 2)
    {
        if (string.IsNullOrEmpty(text))
            return;

        var x = (int)position.X;
        var y = (int)position.Y;
        var charAdvance = 6 * scale;
        var lineAdvance = 9 * scale;

        foreach (var rawChar in text)
        {
            if (rawChar == '\n')
            {
                x = (int)position.X;
                y += lineAdvance;
                continue;
            }

            var c = char.ToUpperInvariant(rawChar);
            if (!Glyphs.TryGetValue(c, out var glyph))
                glyph = Glyphs['?'];

            DrawGlyph(spriteBatch, glyph, x, y, color, scale);
            x += charAdvance;
        }
    }

    private void DrawGlyph(SpriteBatch spriteBatch, string[] glyph, int x, int y, Color color, int scale)
    {
        for (var row = 0; row < glyph.Length; row++)
        {
            for (var col = 0; col < glyph[row].Length; col++)
            {
                if (glyph[row][col] != '1')
                    continue;

                spriteBatch.Draw(_pixel, new Rectangle(x + col * scale, y + row * scale, scale, scale), color);
            }
        }
    }
